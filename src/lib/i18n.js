export function formatNumber(num, lang) {
  if (lang !== 'ar') return String(num);
  return String(num).replace(/\d/g, d => '\u0660\u0661\u0662\u0663\u0664\u0665\u0666\u0667\u0668\u0669'[parseInt(d)]);
}
