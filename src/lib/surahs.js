export const SURAHS = SURAH_NAMES.map((s, i) => ({
  number: i + 1,
  name: s.en,
  arabic: s.ar,
  startPage: SURAH_START_PAGES[i],
}));

export function getSurahList() {
  return Promise.resolve(SURAHS);
}

export function findSurahByPage(page) {
  let best = SURAHS[0];
  for (const s of SURAHS) {
    if (s.startPage <= page) best = s;
    else break;
  }
  return best;
}
